<?php
// Example PHP code to demonstrate some basic operations and variables

$first_number = 5;
$second_number = 3;

// Adding two numbers
$total = $first_number + $second_number;
echo "The sum of $first_number and $second_number is: " . $total . "\n";

// Subtracting one number from another
$difference = $first_number - $second_number;
echo "The difference between $first_number and $second_number is: " . $difference . "\n";

// Multiplying two numbers
$product = $first_number * $second_number;
echo "The product of $first_number and $second_number is: " . $product . "\n";

// Dividing one number by another
$quotient = $first_number / $second_number;
echo "The quotient of $first_number divided by $second_number is: " . $quotient . "\n";
?>
