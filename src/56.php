<?php
// This PHP script demonstrates basic arithmetic operations.
$num1 = 5;
$num2 = 3;

$result = $num1 + $num2; // Addition operation

echo "The sum of $num1 and $num2 is: " . $result;

$result = $num1 - $num2; // Subtraction operation

echo "The difference between $num1 and $num2 is: " . $result;

$result = $num1 * $num2; // Multiplication operation

echo "The product of $num1 and $num2 is: " . $result;
